"""
Pipeline Orchestrator — Design + Corruption case generation via OpenRouter API.

Architecture (ARCH-1, 2026-04-08):
  Cases are generated by a two-node pipeline, not extracted from source papers.

  Stage 1  Hypothesis Generator  — one LLM call per case, concurrent
  Stage 2  Sound Design Writer   — generates a methodologically correct experiment plan
  Stage 3  Corruption Node       — inserts N flaws (N sampled by orchestrator, not LLM)
  Stage 4  Ground Truth Assembler — structures corruption diff into case metadata
  Stage 5  Smoke Test            — single-pass Sonnet evaluation; gate on discriminating range

  Corruption level is sampled by Python before Stage 3 is called:
    P(0 flaws)   = 0.25  → sound design, correct verdict = "approve"
    P(1 flaw)    = 0.35  → one subtle corruption
    P(2 flaws)   = 0.25  → compound or independent corruptions
    P(many)      = 0.15  → 3-5 corruptions, includes calibration anchors

  Ground truth is automatic: the pre-corruption design is always correct;
  the corruption node's diff report is the answer key.

Usage:
    uv run pipeline/orchestrator.py \\
        --batch-size 15 \\
        --start-case-id 1

Models are configurable per stage via --stage1-model, --stage3-model, etc.
Code (not prompts) controls corruption level. Python loop + ThreadPoolExecutor.
"""

